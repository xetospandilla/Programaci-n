//----------------------------------------------------
#include <iostream>
using namespace std;

int main(){

    for(int i = 0; i < 10; i++){
        cout << i + 1 << endl;
    }


    int nums[] = {1,2,3,4,5};

    for(int i = 0; i < 5; i++){
        cout << "Números: " << nums[i] << ", ";
    }
    cout << endl;

    return 0;
}
