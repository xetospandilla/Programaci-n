#include <iostream> //Introduce una libreria y permite el acceso a su contenido.
using namespace std; //Hace que todos los outputs y inputs dentro del código sean strings.

//-----------------------------------------------------------------------------------------------------

int main (){ //Abre la función principal del programa. "Main" es una función tipo entera en este caso.
    
    cout << "Hola, que tal?" << endl;
    /* 
    cout -> Indica que lo posterior se imprimira en consola.
    En este caso en especídico el string "Hola, que tal?" será lo que se imprimirá en consola.
    endl -> Indica el final de un output con un salto de línea resperto al siguiente.
    */
    return 0; 
    /* 
    return -> nos devuelve un valor.
    Normalmente suele ser el resultado de los cálculos o lo que queremos que nos devuelva una función.
    
    En este caso es una variable de tipo entero "0"
    */
}
